package cp17304_n3.fpoly.du_an_1.DTO;

public class Sach {
    int idSach;
    int idNxb;
    String tenSach;
    int gia;

    public int getIdSach() {
        return idSach;
    }

    public void setIdSach(int idSach) {
        this.idSach = idSach;
    }

    public int getIdNxb() {
        return idNxb;
    }

    public void setIdNxb(int idNxb) {
        this.idNxb = idNxb;
    }

    public String getTenSach() {
        return tenSach;
    }

    public void setTenSach(String tenSach) {
        this.tenSach = tenSach;
    }

    public int getGia() {
        return gia;
    }

    public void setGia(int gia) {
        this.gia = gia;
    }
}
